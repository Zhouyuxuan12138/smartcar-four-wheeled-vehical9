/**********************************************************************************************************************
*  @brief      AD采集函数
*  @return     void                ADC采集数值
*  @since      v1.1
*  Sample usage:                  LV_Sample();
**********************************************************************************************************************/
void LV_Sample(void);
/**********************************************************************************************************************
*  @brief      对采集值滤波
*  @return     void                滤波后的数值
*  @since      v1.1
*  Sample usage:                  LV_Get_Val();
**********************************************************************************************************************/
void LV_Get_Val(void);
/**********************************************************************************************************************
*  @brief      将采集电感值排序
*  @return     void         排序后的值
*  @since      v1.1
*  Sample usage:                   LV_Sort();
**********************************************************************************************************************/
void LV_Sort(void);
